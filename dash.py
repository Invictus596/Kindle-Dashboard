def invert_pulse():
 for i in range(len(buf)): buf[i]=W0-buf[i]
 with open('/tmp/d.png','wb') as f: f.write(png(buf,W,H))
 os.system('/usr/sbin/eips -g /tmp/d.png 2>/dev/null')
