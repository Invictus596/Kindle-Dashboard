def px(x,y,c):
 if 0<=x<W and 0<=y<H: buf[y*W+x]=c
def fill(x,y,w,h,c):
 for dy in range(h):
  oy=y+dy
  for dx in range(w):
   ox=x+dx
   if 0<=ox<W and 0<=oy<H: buf[oy*W+ox]=c
def circle(cx,cy,r,c):
 for dy in range(-r,r+1):
  for dx in range(-r,r+1):
   if dx*dx+dy*dy<=r*r: px(cx+dx,cy+dy,c)

