def clock_bottom():
 sc=20 if W==800 else 15; return H//6+sc*8+30

def greeting(h):
 n=CFG.get('name','') or 'there'
 if h<5:  return f"It's late, {n}"
 if h<12: return f"Good morning, {n}"
 if h<17: return f"Good afternoon, {n}"
 if h<21: return f"Good evening, {n}"
 return f"It's late, {n}"

def text(x,y,s,c=B,sc=1):
 for ch in s:
  r=F.get(ch)
  if not r: x+=8*sc; continue
  for ri in range(8):
   row=r[ri]
   if row:
    for ci in range(8):
     if row & (1<<(7-ci)):
      if sc==1: px(x+ci,y+ri,c)
      else: fill(x+ci*sc,y+ri*sc,sc,sc,c)
  x+=8*sc

def tw(s):
 return sum(8 for ch in s if ch in F)

# --- clock digits using 8x8 font at large scale ---
def draw_clock(hs,ms,ap):
 sc = 20 if W==800 else 15
 ch = sc*8
 cw = sc*8
 gap = sc*2
 total_w = cw*4 + gap*2
 ox = (W - total_w)//2
 oy = H//6 + 30  # shift down for greeting
 cx = ox + cw*2 + gap  # colon center

 text(ox, oy, hs, B, sc)
 # colon — two small dots centered between HH and MM
 cy = oy + ch//2
 cr = sc//2+1
 circle(cx, cy - ch//4, cr, B)
 circle(cx, cy + ch//4, cr, B)
 text(cx + gap, oy, ms, B, sc)

 # AM/PM to the right of MM
 asc = 2 if W==800 else 1
 ax = ox + total_w + 4
 text(ax, oy + ch//2 - asc*8//2, ap, B, asc)

 return oy + ch

